# Пример создания списка
my_list = [1, 2, 3, 4, 5]
print(my_list)

# Добавление элемента в список
my_list.append(6)
print(my_list)

# Удаление элемента из списка
my_list.remove(3)
print(my_list)

# Доступ к элементу по индексу
print(my_list[2])

